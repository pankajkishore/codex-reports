import { writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { itineraryDays, locations } from "./route-data.mjs";

const outputPath = fileURLToPath(
  new URL("./route-geometries.mjs", import.meta.url),
);
const osrmBase =
  process.env.OSRM_URL ?? "https://router.project-osrm.org";
const output = {};

for (const [dayId, day] of Object.entries(itineraryDays)) {
  const coordinates = day.route
    .map((locationId) => {
      const location = locations[locationId];
      return `${location.lng},${location.lat}`;
    })
    .join(";");
  const url =
    `${osrmBase}/route/v1/driving/${coordinates}` +
    "?overview=full&geometries=geojson&steps=false";
  const response = await fetch(url, {
    headers: { "User-Agent": "Codex Yellowstone itinerary route generator" },
  });

  if (!response.ok) {
    throw new Error(`${dayId}: OSRM returned HTTP ${response.status}`);
  }

  const payload = await response.json();
  if (payload.code !== "Ok" || !payload.routes?.[0]) {
    throw new Error(`${dayId}: OSRM route failed with ${payload.code}`);
  }

  const route = payload.routes[0];
  output[dayId] = {
    source: "OSRM public API using OpenStreetMap road data",
    distanceMeters: Math.round(route.distance),
    durationSeconds: Math.round(route.duration),
    coordinates: route.geometry.coordinates.map(([lng, lat]) => [
      Number(lng.toFixed(6)),
      Number(lat.toFixed(6)),
    ]),
    legs: route.legs.map((leg) => ({
      distanceMeters: Math.round(leg.distance),
      durationSeconds: Math.round(leg.duration),
    })),
    snappedWaypoints: payload.waypoints.map((waypoint) => ({
      name: waypoint.name,
      location: waypoint.location.map((value) =>
        Number(value.toFixed(6)),
      ),
      distanceMeters: Math.round(waypoint.distance),
    })),
  };

  console.log(
    `${dayId}: ${(route.distance / 1609.344).toFixed(1)} mi, ` +
      `${(route.duration / 3600).toFixed(1)} hr`,
  );
}

const contents =
  "// Generated by generate-route-geometries.mjs. Do not edit manually.\n" +
  `export const generatedAt = ${JSON.stringify(new Date().toISOString())};\n` +
  `export const routeGeometries = ${JSON.stringify(output)};\n`;

await writeFile(outputPath, contents, "utf8");
